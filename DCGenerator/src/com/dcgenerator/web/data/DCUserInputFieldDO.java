package com.dcgenerator.web.data;

import java.util.Vector;

public class DCUserInputFieldDO {
	//Required field to display user input fields on screen
	private	int 	fieldNumber;
	private	boolean	fieldRequired;
	private	String	fieldId;
	private	String	fieldType;
	private	String	fieldValue;
	private	String	fieldLabel;
	
	private	boolean	fieldDefaultState; //Mandatory in case fieldType is check box
	
	private	Vector 	fieldOptionList; //Mandatory in case fieldType is drop down
	
	//Getter/Setter method for FieldNumber
	public void setFieldNumber(int fieldNumber){
		this.fieldNumber = fieldNumber;
	}
	
	public int getFieldNumber(){
		return fieldNumber;
	}
	
	//Getter/Setter method for FieldNumber
	public void setFieldRequired(boolean fieldRequired){
		this.fieldRequired = fieldRequired;
	}
	
	public boolean getFieldRequired(){
		return fieldRequired;
	}
	
	//Getter/Setter method for FieldId
	public void setFieldId(String fieldId){
		this.fieldId = fieldId;
	}
	
	public String getFieldId(){
		return fieldId;
	}
	
	//Getter/Setter method for FieldType
	public void setFieldType(String fieldType){
		this.fieldType = fieldType;
	}
	
	public String getFieldType(){
		return fieldType;
	}
	
	//Getter/Setter method for FieldValue
	public void setFieldValue(String fieldValue){
		this.fieldValue = fieldValue;
	}
	
	public String getFieldValue(){
		return fieldValue;
	}
	
	//Getter/Setter method for FieldLabel
	public void setFieldLabel(String fieldLabel){
		this.fieldLabel = fieldLabel;
	}
	
	public String getFieldLabel(){
		return fieldLabel;
	}
	
	//Getter/Setter method for FieldNumber
	public void setFieldDefaultState(boolean fieldDefaultState){
		this.fieldDefaultState = fieldDefaultState;
	}
	
	public boolean getFieldDefaultState(){
		return fieldDefaultState;
	}
	
	//Getter/Setter method for FieldNumber
	public void setFieldOptionList(Vector fieldOptionList){
		this.fieldOptionList = fieldOptionList;
	}
	
	public Vector getFieldOptionList(){
		return fieldOptionList;
	}
	
	//Overwrite toString method
	public String toString(){
		StringBuffer dcUserInputFieldDO = new StringBuffer("<DCUserInputFieldDO>\n");
		dcUserInputFieldDO.append("<FieldNumber>" + getFieldNumber() + "</FieldNumber>\n");
		dcUserInputFieldDO.append("<FieldRequired>" + getFieldRequired() + "</FieldRequired>\n");
		dcUserInputFieldDO.append("<FieldId>" + getFieldId() + "</FieldId>\n");
		dcUserInputFieldDO.append("<FieldType>" + getFieldType() + "</FieldType>\n");
		dcUserInputFieldDO.append("<FieldValue>" + getFieldValue() + "</FieldValue>\n");
		dcUserInputFieldDO.append("<FieldLabel>" + getFieldLabel() + "</FieldLabel>\n");
		dcUserInputFieldDO.append("<FieldDefaultState>" + getFieldDefaultState() + "</FieldDefaultState>\n");
		dcUserInputFieldDO.append("<FieldOptionList>" + getFieldOptionList() + "</FieldOptionList>\n");
		dcUserInputFieldDO.append("</DCUserInputFieldDO>\n");
		return dcUserInputFieldDO.toString();
	}
}
